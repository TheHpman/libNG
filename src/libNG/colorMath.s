_computePalette:
		move.b	PH_ACC(a1), d0		;* d0= table index
		moveq.l	#3, d4			;* speed bits
		and.w	PH_ACC(a1), d4		;* word read, speed in cmd lsb
		move.b	PAL_INC(pc,d4.w), d5	;* d5= index mod
		add.b	d5, PH_ACC(a1)		;* acc+step
		bcs.s	_fadeOver

		move.b	#1, palCommandPending	;* command not done
		moveq.l	#7, d5
		and.b	d0, d5
		bne	_iterateCmd

		move.l	a1, -(sp)
	#if	BANKING_ENABLE
		move.b	PH_PALADDR(a1), REG_BANKING
	#endif
		move.l	PH_PALADDR(a1), a1
		addq.w	#2, a0		;* skip color #0
		addq.w	#2, a1
		add.w	d0, d0
		add.w	d0, d0
		lea.l	(a4,d0.w), a4
		lea.l	(a5,d0.w), a5
		lea.l	(a6,d0.w), a6

		moveq.l	#0xf, d0
		moveq.l	#15-1, d5

;* DRGB RRRR GGGG BBBB
_computeColor:	move.b	(a1), d2		;* color MSB
		and.w	d0, d2		
		move.w	(a1)+, d1
		move.w	d1, d3
		move.b	d3, d4
		add.w	d1, d1
		add.w	d1, d1
		addx.b	d2, d2			;* d2 = R
		lsr.b	#4, d3
		and.w	d0, d3
		add.w	d1, d1
		addx.b	d3, d3			;* d3 = G
		and.w	d0, d4
		add.w	d1, d1
		addx.b	d4, d4			;* d4 = B
		move.b	(a4,d2.w), d2		;* R color index
		move.b	(a5,d3.w), d3		;* G color index
		move.b	(a6,d4.w), d4		;* B color index
		move.w	-64(a3,d2.w), d1	;* R value
		or.w	(a3,d3.w), d1		;* +G value
		or.w	64(a3,d4.w), d1		;* +B value
		move.w	d1, (a0)+
		dbra	d5, _computeColor	;* 12 sum 182cl= 15us
						;* 1 pal 230us

		move.w	#1, (a2)
		move.l	(sp)+, a1
		bra.w	_iterateCmd2

